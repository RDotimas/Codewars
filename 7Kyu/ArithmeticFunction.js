function arithmetic(a, b, operator){
    switch (operator){
      case operator = 'add':
        return a + b
      break;
      case operator = 'subtract':
        return a - b
      break;
      case operator = 'multiply':
        return a * b
      break;
      case operator = 'divide':
        return a / b
      break;
      }
  }